    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        try {
            boolean aux = false;
            Cuenta objAux = new Cuenta();
            objAux = objCue;
            objCue = objManCue.RetiroCuenta(objCue, Double.parseDouble(this.jTextMonto.getText()));
            if (objCue.getSaldo() != objAux.getSaldo()) {
                JOptionPane.showMessageDialog(rootPane, "Saldo Menor al Minimo\n"
                        + "Ingrese valor a retirar nuevamenete");
                aux = true;
            }
            if (aux = true) {
                objTra = objManTra.CrearTransaccion(objCli, objCaj, objCue, ArrayTransacciones);
            }
            this.jcajID.setText(String.valueOf(objTra.getId_transaccion()));
            ArrayTransacciones.add(objTra);
            ArrayCuentas.set(ArrayCuentas.indexOf(objCue), objCue);
            try {
                objManTra.GuardaTransaccion(ArrayTransacciones);
                objManCue.GuardarCuenta(ArrayCuentas);
            } catch (IOException ex) {
                Logger.getLogger(Retiro.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (NumberFormatException e) {

        } finally {
            //TextFields en vacio de nuevo
            this.jTextcuenta.setText("");
            this.jTextCedula.setText("");
            this.jnombre.setText("");
            this.jsaldo.setText("");
            this.jTextMonto.setText("");
            this.jTextfecha.setText("");
            //Desactivar Botones
            this.jButton2.setEnabled(false);
            this.jButton3.setEnabled(false);

            //Desactivar TextFields
            this.jnombre.setEnabled(false);
            this.jsaldo.setEnabled(false);
            this.jTextMonto.setEnabled(false);
            this.jTextfecha.setEnabled(false);

        }
    }//GEN-LAST:event_jButton2MouseClicked

