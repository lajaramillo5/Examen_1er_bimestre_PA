    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        objCue = objManCue.BuscarCuenta(ArrayCuentas, this.jTextcuenta.getText());
        objCli = objManCli.BuscarCliente(ArrayClientes, this.jTextCedula.getText());
        try {
            if (objCue.getNumero().equals(this.jTextcuenta.getText()) && objCue.getCliente().getCedula().equals(this.jTextCedula.getText())) {
                this.jsaldo.setText(String.valueOf(objCue.getSaldo()));
                this.jnombre.setText(objCli.getNombre());
                this.jTextMonto.setEnabled(true);
                this.jTextfecha.setEnabled(true);
                this.jButton3.setEnabled(true);

            } else {
               JOptionPane.showMessageDialog(rootPane, "No es el titular de la cuenta");
                this.jTextcuenta.setText("");
                this.jTextCedula.setText("");
            }
        } catch (Exception e) {
          JOptionPane.showMessageDialog(rootPane, "No es el titular de la cuenta");
                this.jTextcuenta.setText("");
                this.jTextCedula.setText("");
        }

    }//GEN-LAST:event_jButton1MouseClicked

