    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        Expresiones objexp = new Expresiones();
        String S = "Cedula: " + this.jTextCedula.getText() + "\n Beneficiario:" + this.jnombre.getText() + "\n Cuenta:" + this.jTextcuenta.getText() + "\n Saldo:" + this.jsaldo.getText()
                + "\n Monto:" + this.jTextMonto.getText()
                + "\n Fecha:" + this.jTextfecha.getText()
                + "\n";
        String fecha = this.jTextfecha.getText();
        this.jTextArea1.setText(S);
        if (objexp.fecha(fecha)) {
            JOptionPane.showMessageDialog(null, "fecha  correcta");
            this.jButton2.setEnabled(true);
            this.jTextArea1.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Fecha no correcta");
            this.jButton2.setEnabled(false);
            this.jTextArea1.setEnabled(false);

        }

        if (objexp.soloNumeros(this.jTextMonto.getText())) {
            JOptionPane.showMessageDialog(null, "ingrese solo digitos");
            this.jButton2.setEnabled(false);
            this.jTextArea1.setEnabled(false);

        }

        double saldito = Double.parseDouble(this.jsaldo.getText());
        switch (objCue.getOpcion()) {
            case 1:
                saldito = saldito - 20;
                if (saldito <= Double.parseDouble(this.jTextMonto.getText())) {
                    JOptionPane.showMessageDialog(null, "No tienes saldo suficiente");
                    this.jButton2.setEnabled(false);
                    this.jTextArea1.setEnabled(false);
                }
                break;
            case 2:
                saldito = saldito - 50;
                if (saldito <= Double.parseDouble(this.jTextMonto.getText())) {
                    JOptionPane.showMessageDialog(null, "No tiene saldo suficiente");
                    this.jButton2.setEnabled(false);
                    this.jTextArea1.setEnabled(false);

                }
                break;
        }


    }//GEN-LAST:event_jButton3MouseClicked

